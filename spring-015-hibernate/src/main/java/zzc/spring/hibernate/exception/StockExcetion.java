package zzc.spring.hibernate.exception;

public class StockExcetion extends RuntimeException {

	public StockExcetion() {
		super();
	}

	public StockExcetion(String message, Throwable cause) {
		super(message, cause);
	}

	public StockExcetion(String message) {
		super(message);
	}

	public StockExcetion(Throwable cause) {
		super(cause);
	}

	
	
}
